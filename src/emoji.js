const generateEmojiHash = (str, length = 8) => {
  if (!str || str.length === 0) {
    return ''
  }

  // Generate a hash from the string
  let hash = 0
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i)
    hash = ((hash << 5) - hash) + char
    hash = hash & hash // Convert to 32bit integer
  }

  // Generate emoji hash - returns array so length is guaranteed
  let emojiHash = '';
  let currentHash = Math.abs(hash)

  for (let i = 0; i < length; i++) {
    // Use a different seed for each position to get varied emojis
    const seed = currentHash + i * 2654435761 // Use a prime multiplier for distribution
    const index = seed % emojis.length
    emojiHash = add(emojiHash, emojis[index])

    // Update hash for next iteration using a mixing function
    currentHash = ((currentHash * 1103515245) + 12345) & 0x7fffffff
  }

  return emojiHash;
}

